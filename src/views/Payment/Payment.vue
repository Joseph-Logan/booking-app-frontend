  visa_single,
  diners_single,
  discover_single,
  jcb_single,
  maestro_single,
  mastercard_single,
  unionpay_single,
} from "@/utils/payment.js";

import { USD, MONTH, YEAR } from '@/utils/strings'
import Storage from '@/utils/storage'

export default {
  data: () => ({
    name: "",
    cardNumber: "",
    expirationDate: "",
    securityCode: "",
  }),
  computed: {
    ...mapGetters({
      isLoading: 'membership/getIsLoading',
      message: 'membership/getMessage',
      status: 'membership/getStatus'
    }),
  },
  methods: {
    async handleSubmit () {
      try {
        let data = {
          card_credentials: {
            amount: 7.99,
            description: "Compra de membresia",
            entity_description: "Compra de membresia",
            currency: USD,
            credit_card_number: this.cardNumber.substring(0,19),
            credit_card_security_code_number: this.securityCode,
            exp_month: await this.subStringDate(this.expirationDate, MONTH),
            exp_year: await this.subStringDate(this.expirationDate, YEAR)
          },
          membership_data: {
            endDate: this.handleMonthExpired(),
            description: "Compra de membresia"
          },
          user_data: {
            id: JSON.parse(Storage.getItem('user'))._id
          }
        }

        // DISPATCH
        this.$store.dispatch('membership/handleLoading', true)
        this.$store.dispatch('membership/purchaseMembership', data)

        this.handleWatchNotify()
      } catch (err) {
        this.handleNotify(500, 'Mensaje', err)
      }
    },
    handleWatchNotify () {
      let watch = this.$store.watch((state, getters) => {
        if (!getters['membership/getIsLoading']) {
          console.log(this.message)
          this.handleNotify(this.status ,'Mensaje', this.message)
        }
      })
      
      setTimeout(() => {
        watch()
      }, 4000)
    },
    handleNotify (status, title, text) {
      if (status === 201) {
        this.$vs.notification({
          title, 
          text, 
          position: 'top-center', 
          color: 'primary'
        })
        return
      }
      let color = 'danger'
      this.$vs.notification({
        title: title, 
        text: text, 
        position: 'top-right', 
        color: color, 
      })
    },
    handleName(value) {
      this.name = value;
      if (value.length == 0) {
        document.getElementById("svgname").innerHTML = "John Doe";
        document.getElementById("svgnameback").innerHTML = "John Doe";
      } else {
        document.getElementById("svgname").innerHTML = value;
        document.getElementById("svgnameback").innerHTML = value;
      }
    },
    subStringDate (date, type) {
      let dateFormat = date.split('/')
      let result = null
      switch (type) {
        case MONTH: 
          result = dateFormat[0].substring(0,2)
          break;
        
        case YEAR:
          result = dateFormat[1].substring(0,2)
          break;
      }
      return result
    },
    handleMonthExpired() {
      let date = new Date()
      date.setMonth(date.getMonth() + 2)
      
      let format = `${date.getFullYear()}-${this.zeroPad(date.getMonth() + 1)}-${this.zeroPad(date.getDate())}`
      return format
    },
    zeroPad (num) {
      if (num >= 0 || num <= 9) {
        return num.toString().padStart(2, "0");
      }
      return num
    },
    handleCardNumber(value) {
      this.cardNumber = value;
    },
    handleDateExpiration(value) {
      this.expirationDate = value;
    },
    handleSecurityCode(value) {
      this.securityCode = value;
    },
    onload() {
      const ccicon = document.getElementById("ccicon");
      const ccsingle = document.getElementById("ccsingle");
      let cardnumber = document.getElementById("vs-input--card-number");
      let expirationdate = document.getElementById("vs-input--expiration-date");
      let securitycode = document.getElementById("vs-input--security-code");
      let name = document.getElementById("vs-input--name");

      name.addEventListener("focus", function() {
        document.querySelector(".creditcard").classList.remove("flipped");
      });

      cardnumber.addEventListener("focus", function() {
        document.querySelector(".creditcard").classList.remove("flipped");
      });

      expirationdate.addEventListener("focus", function() {
        document.querySelector(".creditcard").classList.remove("flipped");
      });

      securitycode.addEventListener("focus", function() {
        document.querySelector(".creditcard").classList.add("flipped");
      });

      // CREDIT CARD IMAGE JS
      document.querySelector(".preload").classList.remove("preload");
      document
        .querySelector(".creditcard")
        .addEventListener("click", function() {
          if (this.classList.contains("flipped")) {
            this.classList.remove("flipped");
          } else {
            this.classList.add("flipped");
          }
        });

      const swapColor = (basecolor) => {
        document.querySelectorAll(".lightcolor").forEach(function(input) {
          input.setAttribute("class", "");
          input.setAttribute("class", "lightcolor " + basecolor);
        });
        document.querySelectorAll(".darkcolor").forEach(function(input) {
          input.setAttribute("class", "");
          input.setAttribute("class", "darkcolor " + basecolor + "dark");
        });
      };

      let maskCardNumberAction = maskCardNumber(cardnumber);
      let maskExpirationDateAction = maskExpirationDate(expirationdate);
      let maskSecurityCodeAction = maskSecurityCode(securitycode);
      //pop in the appropriate card icon when detected
      maskCardNumberAction.on("accept", function() {
        switch (maskCardNumberAction.masked.currentMask.cardtype) {
          case "american express":
            ccicon.innerHTML = amex;
            ccsingle.innerHTML = amex_single;
            swapColor("green");
            break;
          case "visa":
            ccicon.innerHTML = visa;
            ccsingle.innerHTML = visa_single;
            swapColor("lime");
            break;
          case "diners":
            ccicon.innerHTML = diners;
            ccsingle.innerHTML = diners_single;
            swapColor("orange");
            break;
          case "discover":
            ccicon.innerHTML = discover;
            ccsingle.innerHTML = discover_single;
            swapColor("purple");
            break;
          case "jcb" || "jcb15":
            ccicon.innerHTML = jcb;
            ccsingle.innerHTML = jcb_single;
            swapColor("red");
            break;
          case "maestro":
            ccicon.innerHTML = maestro;
            ccsingle.innerHTML = maestro_single;
            swapColor("yellow");
            break;
          case "mastercard":
            ccicon.innerHTML = mastercard;
            ccsingle.innerHTML = mastercard_single;
            swapColor("lightblue");

            break;
          case "unionpay":
            ccicon.innerHTML = unionpay;
            ccsingle.innerHTML = unionpay_single;
            swapColor("cyan");
            break;
          default:
            ccicon.innerHTML = "";
            ccsingle.innerHTML = "";
            swapColor("grey");
            break;
        }
      });

      maskCardNumberAction.on("accept", function() {
        if (maskCardNumberAction.value.length == 0) {
          document.getElementById("svgnumber").innerHTML =
            "0123 4567 8910 1112";
        } else {
          document.getElementById("svgnumber").innerHTML =
            maskCardNumberAction.value;
        }
      });

      maskExpirationDateAction.on("accept", function() {
        if (maskExpirationDateAction.value.length == 0) {
          document.getElementById("svgexpire").innerHTML = "01/23";
        } else {
          document.getElementById("svgexpire").innerHTML =
            maskExpirationDateAction.value;
        }
      });

      maskSecurityCodeAction.on("accept", function() {
        if (maskSecurityCodeAction.value.length == 0) {
          document.getElementById("svgsecurity").innerHTML = "985";
        } else {
          document.getElementById("svgsecurity").innerHTML =
            maskSecurityCodeAction.value;
        }
      });
    },
  },
  components: {
    Input,
  },
  mounted() {
    this.onload();
  },
};
</script>

